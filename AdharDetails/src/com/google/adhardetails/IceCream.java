package com.google.adhardetails;

public class IceCream {

	private int iceNO;
	private String iceName;
	private String iceFlavour;
	private double icePrice;
	public int getIceNO() {
		return iceNO;
	}
	public void setIceNO(int iceNO) {
		this.iceNO = iceNO;
	}
	public String getIceName() {
		return iceName;
	}
	public void setIceName(String iceName) {
		this.iceName = iceName;
	}
	public String getIceFlavour() {
		return iceFlavour;
	}
	public void setIceFlavour(String iceFlavour) {
		this.iceFlavour = iceFlavour;
	}
	public double getIcePrice() {
		return icePrice;
	}
	public void setIcePrice(double icePrice) {
		this.icePrice = icePrice;
	}
	
	
	
}
